package com.oops;

public class Math {

	public void multiply (int a, int b) {
		System.out.println(a+ "x"+ b+"=" +(a*b));
	}
	
	// we can change the number of parameters 
	
	public void multiply (int a, int b, int c) {
		System.out.println(a+" x "+b+"x"+"c"+ "=" +(a*b*c));
	}
	// we can change the order of parameters 
	// we can change the types of variable
	
}
